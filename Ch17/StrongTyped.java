public class StrongTyped {

	public static void main(String[] args) {
		double a = 5;
		System.out.println(a);
		double b = 3 * 5;
		System.out.println(b);		
		double x = 5 / 3;
		System.out.println(x);
		double y = (double)(5 / 3);
		System.out.println(y);
		double z = (double)5 / 3;
		System.out.println(z);
		double t = 5.0 / 3;
		System.out.println(t);
		double u = 5 / 3d;
		System.out.println(u);
	}

}
